public class Test003_변수의_대입 {
	public static void main( String args[] ) {
		int i;
		i = 10;
		System.out.println( i );
	}
}

//	Test003.java 대입은 오른쪽의 값을 왼쪽의 변수에 넣어야 한다.
//	10 = i; 이건 아니다.
/*
변수는 기억공간을 새로이 확보하는 것이고 ...
변수를 선언하면 확보되고 
자료형 이름 의 형태를 띄고 
byte short int long float double boolean char
저장할땐 대입 , 왼쪽이 변수 오른쪽이 값 
값은 자료형이 감당 가능한 범위에서 된다. 
*/